
public class State
{
	private boolean isAlive;
	private boolean explode;
	
	
	
	public void setAlive(boolean isAlive)
	{
		this.isAlive = isAlive;
	}
	
	public boolean getIsAlive()
	{
		return isAlive;
	}
	public void setExplode(boolean status)
	{
		explode = status;
	}
	public boolean getExplode()
	{
		return explode;
	}
}
